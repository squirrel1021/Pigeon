public class Pigeon {
    private String id;

    private String name;

    private String doveCode;

    private String sex;

    private String color;

    private String sandEye;

    private String ancestry;

    private String ringId;

    private String ringCode;

    private String ringName;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDoveCode() {
        return doveCode;
    }

    public void setDoveCode(String doveCode) {
        this.doveCode = doveCode;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getSandEye() {
        return sandEye;
    }

    public void setSandEye(String sandEye) {
        this.sandEye = sandEye;
    }

    public String getAncestry() {
        return ancestry;
    }

    public void setAncestry(String ancestry) {
        this.ancestry = ancestry;
    }

    public String getRingId() {
        return ringId;
    }

    public void setRingId(String ringId) {
        this.ringId = ringId;
    }

    public String getRingCode() {
        return ringCode;
    }

    public void setRingCode(String ringCode) {
        this.ringCode = ringCode;
    }

    public String getRingName() {
        return ringName;
    }

    public void setRingName(String ringName) {
        this.ringName = ringName;
    }
}
